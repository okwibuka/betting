Allegri: Ronaldo has ‘more responsibility’ at Juventus this season<br>
<img src="ronaldo2.jpg" height="150"width="150"><br>
Cristiano Ronaldo
Juventus head coach Massimiliano Allegri says he expects Cristiano Ronaldo to demonstrate 
even greater responsibility this season.

Allegri returned to the club for a second spell in May, having previously guided the 
Bianconeri to five successive Scudettos and two Champions League finals between 2014 and 2019.

The 53-year-old will be looking to help Juve regain the Serie A title after they surrendered their crown to Inter last season.

Question marks remain as to whether or not his attack will be led in the new campaign by Ronaldo,
 with speculation linking him with a move away from Turin.

The Portuguese talisman top-scored in Serie A last term with 29 goals, taking his tally with the 
club to 101 since arriving from Real Madrid in 2018.

Ronaldo, who returned to pre-season training on Monday following an extended break due to his Euro 2020 exertions, 
has one year remaining on his contract at the Allianz Stadium.

Nevertheless, the 36-year-old remains firmly in the plans of Allegri, though the head coach warned that his game 
time may be reduced to accommodate team rotation during another hectic campaign.

“Ronaldo is a great player and a smart guy,” he said. “I spoke to him yesterday, as I did with the others.

“I told him that this is an important season and that I am happy to meet him again.

“Now, he has more responsibility because three years ago, we had a more experienced team, while now there are younger players.

“I expect a lot from him as an experienced player. He is in good condition and he is fit.

“There are different moments in a season, playing one game every three days. I’ll need to rotate the team, 
involving all the players and it also applies to him.”</center>
